<script>
  const tooltips = document.querySelectorAll(".all-tooltip .tooltip");
  const fullDiv = document.querySelector(".section-to");
  const container = document.querySelector(".container-to");
  let timeoutId;

  // Function to show tooltips for 1 second
  function showTooltips() {
    tooltips.forEach((tooltip) => {
      tooltip.classList.add("active");
    });

    setTimeout(() => {
      tooltips.forEach((tooltip) => {
        tooltip.classList.remove("active");
      });
    }, 2000);
  }

  // Intersection Observer to detect when the section is in the viewport
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          // If the section is in the viewport, show the tooltips
          showTooltips();
          observer.unobserve(entry.target); // Stop observing after displaying tooltips
        }
      });
    },
    { threshold: 0.5 } // Adjust the threshold as needed
  );

  window.addEventListener("resize", contentPosition);
  window.addEventListener("DOMContentLoaded", () => {
    contentPosition();
    observer.observe(fullDiv); // Start observing the section
  });

  function contentPosition() {
    tooltips.forEach((tooltip) => {
      const pin = tooltip.querySelector(".pin");
      const content = tooltip.querySelector(".tooltip-content");
      const arrow = tooltip.querySelector(".arrow");
      const pinLeft = pin.offsetLeft;
      const isMobile = window.innerWidth <= 768; // Adjust the breakpoint as needed for mobile view
    
      if (isMobile) {
        // Center the tooltip content on mobile screens
        content.style.left = "50%";
        content.style.transform = "translateX(-50%)";
        content.style.top = pin.offsetTop + 50 + "px"; // Adjust the top offset for mobile
      } else {
        // Desktop view positioning
        if (pinLeft + content.offsetWidth / 2 > fullDiv.offsetWidth) {
          const extraLeft = fullDiv.offsetWidth - (pinLeft + content.offsetWidth / 2);
          content.style.left = pinLeft - content.offsetWidth / 2 + extraLeft - 30 + "px";
          content.style.top = pin.offsetTop + 30 + "px";
        } else if (pin.offsetLeft + container.offsetLeft < content.offsetWidth / 2) {
          content.style.left = -container.offsetLeft + "px";
          content.style.top = pin.offsetTop + 30 + "px";
        } else {
          content.style.left = pinLeft - content.offsetWidth / 2 + "px";
          content.style.top = pin.offsetTop + 30 + "px";
        }
        arrow.style.left = pinLeft - content.offsetLeft + pin.offsetWidth / 2 + "px";
      }
    });
  }

 tooltips.forEach((tooltip) => {
   const pin = tooltip.querySelector(".pin");
   const content = tooltip.querySelector(".tooltip-content");
   pin.addEventListener("mouseover", () => {
     tooltip.classList.add("active");
   });
   pin.addEventListener("mouseleave", () => {
     timeoutId = setTimeout(() => {
       if (!tooltip.classList.contains("content-hover")) {
         tooltip.classList.remove("active");
       }
     }, 2000);
   });
   content.addEventListener("mouseover", () => {
     clearTimeout(timeoutId);
     tooltip.classList.add("active");
     tooltip.classList.add("content-hover");
   });
   content.addEventListener("mouseleave", () => {
     timeoutId = setTimeout(() => {
       tooltip.classList.remove("active");
       tooltip.classList.remove("content-hover");
     }, 2000);
   });
 });
</script>

